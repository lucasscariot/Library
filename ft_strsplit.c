#include "libft.h"

char	**ft_strsplit(char const *s, char c)
{
	char	**str;
	int		i;
	int		j;

	i = 0;
	j = 0;
	str = malloc(1);
	while (s[i] != '\0')
	{
		if (s[i++] == c)
			j++;
	}
	return (str);
}
